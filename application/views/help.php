<ul>
	<li><h3>書籤</h3></li>
	<p>新增書籤時，點擊上排選單中的管理Bookmark，輸入名稱及URL；若給予標籤，從Taglist中拖曳至form中即可。</p>
	<p>修改書籤時，點擊上排選單中的管理Bookmark，將要修改的書籤從清單中拖曳至Form中即可。</p>
	<p>刪除書籤時，將要刪除的書籤拖曳至左排最下面的Delete Form，確定刪除即可。</p>
	<li><h3>標籤</h3></li>
	<p>新增標籤時，點擊上排選單中的管理Tag，輸入名稱及調整顏色後，將Form中的標籤拖曳至Taglist即可。</p>
	<p>修改標籤時，點擊上排選單中的管理Tag，將要修改的標籤拖曳至Form中，修改完成後再將標籤拖曳至Taglist。</p>
	<p>刪除標籤時，將要刪除的標籤從Taglist中拖曳至左排最下面的Delete Form，確定刪除即可。</p>
	<li><h3>搜尋篩選</h3></li>
	<p>搜尋篩選是以標籤為條件，將條件標籤拖曳至Search Form 中，亦可切換篩選模式(AND OR)</p>
